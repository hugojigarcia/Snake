/**
 * Establece las direcciones disponibles en este juego, es decir, arriba, abajo, derecha e izquierda.
 * @author Hugo0133
 *
 */
public enum TipoDireccion {
	/**
	 * Direccion hacia arriba.
	 */
	ARRIBA,
	/**
	 * Direccion hacia la derecha.
	 */
	DERECHA,
	/**
	 * Direccion hacia abajo.
	 */
	ABAJO,
	/**
	 * Direccion hacia la izquierda.
	 */
	IZQUIERDA
}
